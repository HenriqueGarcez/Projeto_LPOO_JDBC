    public void carregarMedicamentos(){

        modeloTabela.setNumRows(0);
        Medicamento_DAO dao = new Medicamento_DAO();
        ArrayList<Medicamento> medicamentos = dao.buscar();
        for(Medicamento medi: medicamentos){
            modeloTabela.addRow(new Object[]{medi.getId(), 
            medi.getNome(),
            medi.getQnt(),
            medi 
            });
            }
        }
    
    public void carregarMedicamentos(String nome) {
            
        modeloTabela.setNumRows(0);
        Medicamento_DAO dao = new Medicamento_DAO();
        ArrayList<Medicamento> medicamentos = dao.buscar(nome);
        for(Medicamento medi: medicamentos){
            modeloTabela.addRow(new Object[]{medi.getId(), 
            medi.getNome(),
            medi.getQnt(),
            medi 
            });
            }
    }

