    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        CadastroMedi_JD telaCadastro = new CadastroMedi_JD(this, rootPaneCheckingEnabled);
        telaCadastro.setVisible(true);
        Medicamento novo = telaCadastro.getMedicamento();
        if (novo != null && novo.getNome() != null && novo.getQnt() != null) {
            Medicamento_DAO dao = new Medicamento_DAO();
            dao.inserir(novo);
            carregarMedicamentos();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao salvar Medicamento");
        }
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int indiceLinha = tblMedicamento.getSelectedRow();
        if(indiceLinha != -1)
        {
            Medicamento selecionado = (Medicamento) modeloTabela.getValueAt(indiceLinha, 3);
            CadastroMedi_JD telaCadastro = new CadastroMedi_JD(this, rootPaneCheckingEnabled);
            telaCadastro.setMedicamento(selecionado);
            telaCadastro.setVisible(true);
            if (selecionado != null && selecionado.getNome() != null && selecionado.getQnt() != null) {
                Medicamento_DAO dao = new Medicamento_DAO();
                dao.editar(selecionado);
                carregarMedicamentos();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao editar Medicamento");
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int indiceLinha = tblMedicamento.getSelectedRow();
        if(indiceLinha != -1){
            Medicamento selecionado = (Medicamento) modeloTabela.getValueAt(indiceLinha, 3);
            Medicamento_DAO dao = new Medicamento_DAO();
            dao.excluir(selecionado);
            carregarMedicamentos();
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

